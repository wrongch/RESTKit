package io.github.wrongch.restkit.toolwindow.action.toolbar;

import com.intellij.openapi.actionSystem.AnActionEvent;
import com.intellij.openapi.actionSystem.ToggleAction;
import com.intellij.openapi.project.Project;
import io.github.wrongch.restkit.config.CommonSettingComponent;
import io.github.wrongch.restkit.toolwindow.RestServiceToolWindow;
import io.github.wrongch.restkit.toolwindow.RestToolWindowFactory;
import org.jetbrains.annotations.NotNull;

/**
 * EnableLibraryAction
 */
public class EnableLibraryAction extends ToggleAction {

    @Override
    public boolean isSelected(@NotNull AnActionEvent e) {
        Project project = e.getProject();
        if (project == null) {
            return false;
        }
        return CommonSettingComponent.getInstance(project).getState().isScanServiceWithLib();
    }

    @Override
    public void setSelected(@NotNull AnActionEvent e, boolean state) {
        Project project = e.getProject();
        if (project == null) {
            return;
        }
        CommonSettingComponent.getInstance(project).getState().setScanServiceWithLib(state);

        RestToolWindowFactory.getRestServiceToolWindow(project, RestServiceToolWindow::scheduleUpdateTree);
    }
}
